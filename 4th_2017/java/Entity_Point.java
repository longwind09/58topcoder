/**
 * 每个点的实体
 * @author shizhongwei
 *
 */
public class Entity_Point {
	
	private int x;
	private int y;
	
	public Entity_Point(int x, int y) {
		super();
		this.x = x;
		this.y = y;
	}

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}
	
	
}
